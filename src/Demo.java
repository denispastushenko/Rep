public class Demo {
    public static double devide(double a, double b) {
        return a/b;
    }

    public static int devide(int a, int b){
        return a/b;
    }

    public static void main(String[] args) {
        System.out.println(devide(12.4, 2.0));
        System.out.println(devide(12, 2));
    }
}
